// ─────────────────────────────────────────────────────────────
// 근로소득공제 누진율 (소득세법 §47) — 2024년 표준 (2026 유지)
// ─────────────────────────────────────────────────────────────
export function earnedIncomeDeduction2026(annualSalary: number): number {
  if (annualSalary <= 5_000_000) return annualSalary * 0.7;
  if (annualSalary <= 15_000_000)
    return 3_500_000 + (annualSalary - 5_000_000) * 0.4;
  if (annualSalary <= 45_000_000)
    return 7_500_000 + (annualSalary - 15_000_000) * 0.15;
  if (annualSalary <= 100_000_000)
    return 12_000_000 + (annualSalary - 45_000_000) * 0.05;
  // 100,000,000 초과 — 14,750,000 + 초과분 2% (단, 한도 20,000,000)
  return Math.min(14_750_000 + (annualSalary - 100_000_000) * 0.02, 20_000_000);
}

// ─────────────────────────────────────────────────────────────
// 종합소득세 산출세액 (누진세율표 적용)
// ─────────────────────────────────────────────────────────────
export function calcIncomeTax2026(taxable: number): number {
  if (taxable <= 0) return 0;
  for (const b of TAX_BRACKETS_2026) {
    if (taxable <= b.limit)
      return Math.max(0, Math.round(taxable * b.rate - b.deduction));
  }
  return 0;
}
